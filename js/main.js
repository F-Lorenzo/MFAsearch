let menuOpen=false;
function toggleMenu(){
  menuOpen=!menuOpen;
  document.getElementById('mobileMenu').classList.toggle('open',menuOpen);
  document.getElementById('hamburger').setAttribute('aria-expanded',menuOpen);
  document.body.style.overflow=menuOpen?'hidden':'';
  const spans=document.querySelectorAll('.nav-hamburger span');
  if(menuOpen){
    spans[0].style.transform='translateY(7px) rotate(45deg)';
    spans[1].style.opacity='0';
    spans[2].style.transform='translateY(-7px) rotate(-45deg)';
  } else {
    spans.forEach(s=>{s.style.transform='';s.style.opacity='';});
  }
}
function closeMenu(){
  menuOpen=false;
  document.getElementById('mobileMenu').classList.remove('open');
  document.getElementById('hamburger').setAttribute('aria-expanded','false');
  document.body.style.overflow='';
  document.querySelectorAll('.nav-hamburger span').forEach(s=>{s.style.transform='';s.style.opacity='';});
}

