let lang='es';

function setLang(l){
  lang=l;
  document.documentElement.setAttribute('lang', l);

  ['btn-es','btn-en'].forEach(id=>{
    const el=document.getElementById(id);
    const active=id==='btn-'+l;
    el.classList.toggle('active',active);
    el.setAttribute('aria-pressed',String(active));
  });
  ['mbtn-es','mbtn-en'].forEach(id=>{
    const el=document.getElementById(id);
    const active=id==='mbtn-'+l;
    el.classList.toggle('active',active);
    el.setAttribute('aria-pressed',String(active));
  });

  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const k=el.getAttribute('data-i18n');
    if(T[l][k]!==undefined) el.innerHTML=T[l][k];
  });

  document.title=T[l].meta_title;
  const metaDesc=document.querySelector('meta[name="description"]');
  if(metaDesc) metaDesc.setAttribute('content',T[l].meta_desc);

  buildFAQ();
  hideFormStatus();
}

function buildFAQ(){
  const list=document.getElementById('faqList');
  list.innerHTML='';
  T[lang].faqs.forEach((item,i)=>{
    const panelId='faq-panel-'+i;
    const btnId='faq-btn-'+i;
    const d=document.createElement('div');
    d.className='faq-item';
    d.innerHTML=`<h3 style="margin:0"><button type="button" class="faq-q" id="${btnId}" aria-expanded="false" aria-controls="${panelId}" onclick="toggleFAQ(${i})"><span>${item.q}</span><span class="faq-icon" aria-hidden="true">+</span></button></h3><div class="faq-a" id="${panelId}" role="region" aria-labelledby="${btnId}">${item.a}</div>`;
    list.appendChild(d);
  });
}

function toggleFAQ(idx){
  document.querySelectorAll('.faq-item').forEach((item,i)=>{
    const btn=item.querySelector('.faq-q');
    if(i===idx){
      const willOpen=!item.classList.contains('open');
      item.classList.toggle('open',willOpen);
      btn.setAttribute('aria-expanded',String(willOpen));
    } else {
      item.classList.remove('open');
      btn.setAttribute('aria-expanded','false');
    }
  });
}

